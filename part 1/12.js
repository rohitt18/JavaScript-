// booleans & comparison operator 
// In JS we mostly use === that checks both values & datatype

// booleans 
// true, false 

let num1 = 7;   // number
let num2 = '7'; // string

console.log(num1<=num2);

// == vs === 
// == only checks value 
// whereas === checks both value and datatype
console.log(num1 === num2);

// != vs !==

console.log(num1 !== num2); 