// break keywork

// continue keyword 

for(let i = 1; i<=10; i++){
    if(i===4){
        break; // 4 ke aate hi loop ke bahar nikal jayega 
    }
    console.log(i); 
}

for(let i = 1; i<=10; i++){
    if(i===4){
        continue; // 4 print nahi hoga and loop aage badh jayega
    }
    console.log(i);
}
